#!/usr/bin/env python

first_number = int(input("Give me the first number: "))
second_number = int(input("Give me the second number: "))
print("Thank you!")
x = first_number
y = second_number
print("x", "+", "y", "=", x + y)
print("x", "-", "y", "=", x - y)
print("x", "/", "y", "=", x / y)
print("x", "*", "y", "=", x * y)